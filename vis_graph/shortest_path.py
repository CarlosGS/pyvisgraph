from vis_graph.graph import Point, Edge, Graph
from vis_graph.visible_vertices import edge_distance
from utils.priority_dict import priority_dict


def dijkstra(graph, origin, destination=None):
    D = {}
    P = {}
    Q = priority_dict()
    Q[origin] = 0

    for v in Q:
        D[v] = Q[v]
        if v == destination: break

        for e in graph[v]:
            w = e.get_adjacent(v)
            elength = D[v] + edge_distance(v, w)
            if w in D:
                if elength < D[w]:
                    raise ValueError
            elif w not in Q or elength < Q[w]:
                Q[w] = elength
                P[w] = v
    return (D, P)


def shortest_path(graph, origin, destination):
    D, P = dijkstra(graph, origin, destination)
    path = []
    while 1:
        path.append(destination)
        if destination == origin: break
        destination = P[destination]
    path.reverse()
    return path
